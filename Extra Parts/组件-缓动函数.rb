#-----------------------------------------------------------
# ○ 使用方法A：通过缓动函数的名称
#-----------------------------------------------------------
#
#      EasingFuction.call("函数名称", t)
#
#   其中 "函数名称" 为缓动函数在参考网站中的名称，
#     如 "easeInSine" 或者 "easeInQuad" ，具体请到参考网站中查阅。
#
#   其中 t 为 0~1 之间的小数，可通过 当前时间 * 1.0 / 全部时间 获得。
#     例如共 10 帧，当前第 1 帧，则传入 t = 1 * 1.0 / 10 = 0.1
#     例如共 20 帧，当前第 16 帧，则传入 t = 16 * 1.0 / 20 = 0.8
#
#-----------------------------------------------------------
# ○ 使用方法B：直接调用某个缓动函数
#-----------------------------------------------------------
#
#      EasingFuction.easeInSine(t)
#
#   其中 easeInSine 可以替换成其他缓动函数的名称。
#
#   其中 t 同上，为 0~1 之间的小数。
#
#-----------------------------------------------------------
# ○ 使用方法C：有多个阶段的缓动
#-----------------------------------------------------------
#
#      EasingFuction.mcall(segments, t)
#
#   其中 segments 是一个数组，
#     它的每一项都是一个数组 [end_time, end_value, easing_name] 。
#
#        end_time   : 本段结束的时间比例 (0~1)
#        end_value  : 本段结束时应达到的值
#        easing_name: 本段使用的缓动函数的名称
#
#     需要有多少个阶段，就填写多少个。
#
#   其中 t 同上，为 0~1 之间的小数。
#
# --- 示例：先加速再缓慢减速 ---
#
#  segments = [
#    [0.3, 0.6, "easeInQuad"],   # 0 → 0.3 从 0 加速到 0.6
#    [1.0, 1.0, "easeOutQuad"]   # 0.3 → 1.0 从 0.6 减速到 1.0
#  ]
#  t = 0.5  # 动画已经进行到 50%
#  v = EasingFuction.mcall(segments, t) # 返回当前时间的比例值
#  
# --- 示例：三段式（启动→匀速→刹车） ---
#
#  segments_3 = [
#    [0.2, 0.3, "easeInCubic"],   # 0 → 0.2 快速启动到 0.3
#    [0.8, 0.9, "linear"],        # 0.2 → 0.8 匀速上升到 0.9
#    [1.0, 1.0, "easeOutQuad"]    # 0.8 → 1.0 缓慢刹停
#  ]
#  t = 0.2  # 动画已经进行到 20%
#  v = EasingFuction.mcall(segments_3, t) # 返回当前时间的比例值

#-----------------------------------------------------------
# ○ 具体案例
#-----------------------------------------------------------
#
#  对 精灵s（Sprite类）的 不透明度（opacity）进行缓动变化：
#
#  1. 不透明度初始 0，最终 255，缓动函数选择 easeInSine 
#
#  2.  t 为当前帧计数，动画一共20帧。
#   （ t 从 0 开始，每帧增加1，直到 20）
#
#  3. 每一帧更新内容为：
#
#    per = EasingFuction.easeInSine( t * 1.0 / 20 )
#    s.opacity = 0 + (255 - 0) * per
#    t += 1
#
#=============================================================================

module EasingFuction

  #--------------------------------------------------------------------------
  # ○ 多段缓动统一调用接口  from DeepSeek
  # @param t [Float] 当前时间比例 (0~1)
  # @param segments [Array<Array>] 分段数组，每个元素为 [end_time, end_value, easing_name]
  #        end_time   : 本段结束的时间比例 (0~1)
  #        end_value  : 本段结束时应达到的值
  #        easing_name: 本段使用的缓动函数的名称
  # @return [Float] 当前 t 对应的值
  #--------------------------------------------------------------------------
  def self.mcall(segments, t)
    # 1. 按结束时间排序
    segs = segments.sort_by { |s| s[0] }
    
    # 2. 边界钳位
    t = 0.0 if t < 0.0
    t = 1.0 if t > 1.0
    
    # 3. 起始状态
    start_time = 0.0
    start_value = 0.0
    
    # 4. 查找 t 所在的分段
    segs.each do |end_time, end_value, easing|
      if t <= end_time
        # 当前分段有效
        duration = end_time - start_time
        if duration <= 0
          # 防止除零，直接返回终点值
          return end_value
        end
        local_t = (t - start_time) / duration
        # 确保 local_t 在 [0,1] 内
        local_t = 0.0 if local_t < 0
        local_t = 1.0 if local_t > 1
        eased = self.call(easing, local_t)  # 0~1 的缓动进度
        # 线性映射到该段的值域
        return start_value + (end_value - start_value) * eased
      end
      # 未命中，更新下一段的起点
      start_time = end_time
      start_value = end_value
    end
    
    # 理论上 t 不会超过最后一段（因为 t ≤ 1 且最后一段 end_time 应 ≥ 1）
    # 但若未找到（例如 segments 为空或最后 end_time < 1），返回最后一段的终点值
    segs.last ? segs.last[1] : 0.0
  end  

  #--------------------------------------------------------------------------
  # ○ 统一的调用接口
  #--------------------------------------------------------------------------
  def self.call(type, x)
    method(type.to_s).call(x)
  end

  #--------------------------------------------------------------------------
  # ○ 缓动函数方法一览
  #--------------------------------------------------------------------------
  def self.linear(x)  # 直线匀速运动
    return x
  end

  def self.easeInSine(x)
    # x 为 0~1 的浮点数，但三角函数需要传入 0~90 度
    return 1 - EAGLE.cos( x * 90 )
  end
  def self.easeOutSine(x)
    return EAGLE.sin( x * 90 )
  end
  def self.easeInOutSine(x)
    return - (EAGLE.cos(x * 90) - 1) / 2
  end

  def self.easeInQuad(x)
    return x * x
  end
  def self.easeOutQuad(x)
    return 1 - (1 - x) * (1 - x)
  end
  def self.easeInOutQuad(x)
    return x < 0.5 ? 2 * x * x : 1 - ((-2 * x + 2)**2) / 2;
  end

  def self.easeInCubic(x)
    return x * x * x;
  end
  def self.easeOutCubic(x)
    return 1 - (1 - x)**3;
  end
  def self.easeInOutCubic(x)
    return x < 0.5 ? (4 * x * x * x) : (1 - ((-2 * x + 2)**3) / 2);
  end

  def self.easeInQuart(x)
    return x * x * x * x;
  end
  def self.easeOutQuart(x)
    return 1 - ((1 - x)**4);
  end
  def self.easeInOutQuart(x)
    return x < 0.5 ? (8 * x * x * x * x) : 1 - ((-2 * x + 2)**4) / 2;
  end

  def self.easeInQuint(x)
    return x * x * x * x * x;
  end
  def self.easeOutQuint(x)
    return 1 - pow(1 - x, 5);
  end
  def self.easeInOutQuint(x)
    return x < 0.5 ? 16 * x * x * x * x * x : 1 - pow(-2 * x + 2, 5) / 2;
  end

  def self.easeInExpo(x)
    return x == 0 ? 0 : pow(2, 10 * x - 10);
  end
  def self.easeOutExpo(x)
    return x == 1 ? 1 : 1 - pow(2, -10 * x);
  end
  def self.easeInOutExpo(x)
    return 0 if x == 0
    return 1 if x == 1
    x < 0.5 ? pow(2, 20 * x - 10) / 2 : (2 - pow(2, -20 * x + 10)) / 2
  end

  def self.easeInCirc(x)
    return 1 - sqrt(1 - pow(x, 2));
  end
  def self.easeOutCirc(x)
    return sqrt(1 - pow(x - 1, 2));
  end
  def self.easeInOutCirc(x)
    return (1 - sqrt(1 - pow(2 * x, 2))) / 2 if x < 0.5
    return (sqrt(1 - pow(-2 * x + 2, 2)) + 1) / 2
  end

  def self.easeInBack(x)
    c1 = 1.70158
    c3 = c1 + 1
    return c3 * x * x * x - c1 * x * x
  end
  def self.easeOutBack(x)
    c1 = 1.70158
    c3 = c1 + 1
    return 1 + c3 * pow(x - 1, 3) + c1 * pow(x - 1, 2)
  end
  def self.easeInOutBack(x)
    c1 = 1.70158
    c2 = c1 * 1.525

    return (pow(2 * x, 2) * ((c2 + 1) * 2 * x - c2)) / 2 if x < 0.5
    return (pow(2 * x - 2, 2) * ((c2 + 1) * (x * 2 - 2) + c2) + 2) / 2
  end

  def self.easeInElastic(x)
    c4 = (2 * Math::PI) / 3;

    return 0 if x == 0
    return 1 if x == 1
    return -pow(2, 10 * x - 10) * EAGLE.sin((x * 10 - 10.75) * c4)
  end
  def self.easeOutElastic(x)
    c4 = (2 * Math::PI) / 3;

    return 0 if x == 0
    return 1 if x == 1
    return pow(2, -10 * x) * EAGLE.sin((x * 10 - 0.75) * c4) + 1
  end
  def self.easeInOutElastic(x)
    c5 = (2 * Math::PI) / 4.5;

    return 0 if x == 0
    return 1 if x == 1
    return -(pow(2, 20 * x - 10) * EAGLE.sin((20 * x - 11.125) * c5)) / 2 if x < 0.5
    return (pow(2, -20 * x + 10) * EAGLE.sin((20 * x - 11.125) * c5)) / 2 + 1
  end

  def self.easeInBounce(x)
    return 1 - easeOutBounce(1 - x)
  end
  def self.easeOutBounce(x)
    n1 = 7.5625
    d1 = 2.75
    return n1 * x * x if x < 1 / d1
    return n1 * (x-=1.5 / d1) * x + 0.75 if x < 2 / d1
    return n1 * (x-=2.25 / d1) * x + 0.9375 if x < 2.5 / d1
    return n1 * (x-=2.625 / d1) * x + 0.984375
  end
  def self.easeInOutBounce(x)
    return (1 - easeOutBounce(1 - 2 * x)) / 2 if x < 0.5
    return (1 + easeOutBounce(2 * x - 1)) / 2
  end

  def self.pow(x, t)
    x ** t
  end
  def self.sqrt(x)
    Math.sqrt(x)
  end
end

