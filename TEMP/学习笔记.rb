#=============================================================================
# 单件方法 - 重定义指定实例中的方法
#=============================================================================
class A
  def a; p 1; end
end

_a = A.new
_a.a # => 1
def _a.a # 单件方法
  A.instance_method(:a).bind(self).call
  p 2
end
_a.a # => 1\n2

_b = A.new
_b.a # => 1
